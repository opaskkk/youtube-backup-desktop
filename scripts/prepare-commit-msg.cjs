const fs = require('node:fs');
const path = require('node:path');
const { execFileSync } = require('node:child_process');

const AUTO_GENERATED_MARKER = '# auto-generated by .githooks/prepare-commit-msg';
const IGNORED_SOURCES = new Set(['merge', 'squash', 'commit']);
const MAX_BODY_ITEMS = 6;

function hasUserContent(message) {
  return String(message || '')
    .split(/\r?\n/)
    .some((line) => {
      const trimmed = line.trim();
      return trimmed && !trimmed.startsWith('#');
    });
}

function shouldAutoGenerate({ source = '', existingMessage = '' }) {
  if (IGNORED_SOURCES.has(source)) {
    return false;
  }

  return !hasUserContent(existingMessage);
}

function classifyPath(filePath) {
  const normalized = String(filePath || '').replace(/\\/g, '/');

  if (!normalized) {
    return '기타';
  }

  if (
    normalized.startsWith('.githooks/')
    || normalized.includes('prepare-commit-msg')
    || normalized.includes('commit-msg')
    || normalized.includes('commitlint')
  ) {
    return '훅/커밋';
  }

  if (
    normalized === 'README.md'
    || normalized === 'CHANGELOG.md'
    || normalized.startsWith('docs/')
    || normalized.endsWith('.md')
  ) {
    return '문서';
  }

  if (normalized.startsWith('test/')) {
    return '테스트';
  }

  if (normalized.startsWith('src/services/')) {
    return '서비스';
  }

  if (normalized.startsWith('src/renderer/')) {
    return '렌더러';
  }

  if (
    normalized === 'src/main.js'
    || normalized === 'src/preload.js'
  ) {
    return '앱';
  }

  if (
    normalized.startsWith('scripts/')
    || normalized.startsWith('build/')
    || normalized === 'package.json'
    || normalized === 'package-lock.json'
    || normalized === '.gitignore'
    || normalized.startsWith('.claude/')
    || normalized.startsWith('.husky/')
  ) {
    return '설정/빌드';
  }

  if (normalized.startsWith('vendor/') || normalized.startsWith('dist/')) {
    return '배포';
  }

  return '기타';
}

function firstUnique(items, limit = 2) {
  const result = [];
  const seen = new Set();

  for (const item of items) {
    if (!item || seen.has(item)) {
      continue;
    }

    seen.add(item);
    result.push(item);
    if (result.length >= limit) {
      break;
    }
  }

  return result;
}

function summarizeMarkdown(diffText) {
  const headings = diffText
    .split(/\r?\n/)
    .filter((line) => /^\+\s*#{1,6}\s+/.test(line))
    .map((line) => line.replace(/^\+\s*/, '').trim());

  const uniqueHeadings = firstUnique(headings, 2);
  if (uniqueHeadings.length > 0) {
    return uniqueHeadings.join(' / ');
  }

  return '본문/목록 수정';
}

function summarizeJson(diffText) {
  const keys = diffText
    .split(/\r?\n/)
    .filter((line) => /^\+\s*"[^"]+"\s*:/.test(line))
    .map((line) => line.replace(/^\+\s*"([^"]+)".*$/, '$1'));

  const uniqueKeys = firstUnique(keys, 2);
  if (uniqueKeys.length > 0) {
    return `${uniqueKeys.join(' / ')} 키 조정`;
  }

  return '설정 값 조정';
}

function summarizeCode(diffText) {
  const summaries = [];

  for (const rawLine of diffText.split(/\r?\n/)) {
    if (!rawLine.startsWith('+') || rawLine.startsWith('+++')) {
      continue;
    }

    const line = rawLine.replace(/^\+\s*/, '');

    let match = line.match(/^(?:async\s+)?function\s+([A-Za-z0-9_$]+)/);
    if (match) {
      summaries.push(`${match[1]} 함수 추가/수정`);
      continue;
    }

    match = line.match(/^class\s+([A-Za-z0-9_$]+)/);
    if (match) {
      summaries.push(`${match[1]} 클래스 추가/수정`);
      continue;
    }

    match = line.match(/^(?:const|let|var)\s+([A-Za-z0-9_$]+)\s*=/);
    if (match) {
      summaries.push(`${match[1]} 선언 추가/수정`);
      continue;
    }

    match = line.match(/^module\.exports\s*=/);
    if (match) {
      summaries.push('export 정리');
      continue;
    }

    if (/require\(/.test(line)) {
      summaries.push('의존성 연결 조정');
      continue;
    }

    if (/on\(['"]/.test(line) || /addEventListener\(/.test(line)) {
      summaries.push('이벤트 처리 조정');
      continue;
    }

    if (/status|progress|warning|error/i.test(line)) {
      summaries.push('상태/오류 처리 조정');
      continue;
    }
  }

  const uniqueSummaries = firstUnique(summaries, 2);
  if (uniqueSummaries.length > 0) {
    return uniqueSummaries.join(' / ');
  }

  return '구현 세부 조정';
}

function summarizeFile(filePath, diffText, binary = false) {
  if (binary) {
    return '바이너리 변경';
  }

  const ext = path.extname(filePath).toLowerCase();
  if (ext === '.md') {
    return summarizeMarkdown(diffText);
  }

  if (ext === '.json') {
    return summarizeJson(diffText);
  }

  if (['.js', '.cjs', '.mjs', '.jsx', '.css', '.html', '.sh', '.ps1'].includes(ext)) {
    return summarizeCode(diffText);
  }

  return '구현 세부 조정';
}

function parseNumstat(output) {
  return String(output || '')
    .split(/\r?\n/)
    .filter(Boolean)
    .map((line) => {
      const [addedRaw = '', deletedRaw = '', ...rest] = line.split('\t');
      const filePath = rest.join('\t');
      const binary = addedRaw === '-' || deletedRaw === '-';
      const added = binary ? 0 : Number.parseInt(addedRaw, 10) || 0;
      const deleted = binary ? 0 : Number.parseInt(deletedRaw, 10) || 0;

      return {
        path: filePath,
        added,
        deleted,
        binary
      };
    });
}

function topLabels(files) {
  const counts = new Map();

  for (const file of files) {
    counts.set(file.label, (counts.get(file.label) || 0) + 1);
  }

  return [...counts.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0], 'ko'))
    .slice(0, 2)
    .map(([label]) => label);
}

function buildAutoCommitMessage(files) {
  const fileCount = files.length;
  const totalAdded = files.reduce((sum, file) => sum + file.added, 0);
  const totalDeleted = files.reduce((sum, file) => sum + file.deleted, 0);
  const labels = topLabels(files);

  let subject = '작업: 변경 요약';
  if (fileCount > 0 && labels.length > 0) {
    subject = `작업: ${labels.join('/')} 업데이트 (${fileCount}건)`;
  } else if (fileCount > 0) {
    subject = `작업: 변경 요약 (${fileCount}건)`;
  }

  const lines = [
    subject,
    '',
    `변경 규모: 파일 ${fileCount}건, +${totalAdded} / -${totalDeleted}`,
    '주요 변경'
  ];

  if (fileCount === 0) {
    lines.push('- 없음');
  } else {
    const entries = [...files]
      .sort((left, right) => {
        const leftWeight = left.added + left.deleted;
        const rightWeight = right.added + right.deleted;
        return rightWeight - leftWeight || left.path.localeCompare(right.path);
      })
      .slice(0, MAX_BODY_ITEMS);

    for (const entry of entries) {
      const suffix = entry.binary ? ' (binary)' : '';
      lines.push(`- [${entry.label}] ${entry.path} (+${entry.added}/-${entry.deleted})${suffix}`);
      lines.push(`  - ${entry.summary}`);
    }

    if (fileCount > MAX_BODY_ITEMS) {
      lines.push(`추가 변경 나머지 ${fileCount - MAX_BODY_ITEMS}건`);
    }
  }

  lines.push('', AUTO_GENERATED_MARKER);
  return `${lines.join('\n')}\n`;
}

function readGitOutput(repoRoot, args) {
  return execFileSync('git', args, {
    cwd: repoRoot,
    encoding: 'utf8'
  });
}

function collectStagedFiles(repoRoot) {
  const numstat = readGitOutput(repoRoot, ['diff', '--cached', '--numstat', '--diff-filter=ACMR']);
  const files = parseNumstat(numstat);

  return files.map((file) => {
    const diffText = readGitOutput(repoRoot, ['diff', '--cached', '--unified=0', '--', file.path]);
    return {
      ...file,
      label: classifyPath(file.path),
      summary: summarizeFile(file.path, diffText, file.binary)
    };
  });
}

function applyPrepareCommitMessage({
  messageFile,
  source = '',
  repoRoot = process.cwd(),
  forceRewrite = false
}) {
  if (!messageFile || !fs.existsSync(messageFile)) {
    return false;
  }

  const existingMessage = fs.readFileSync(messageFile, 'utf8');
  if (!forceRewrite && !shouldAutoGenerate({ source, existingMessage })) {
    return false;
  }

  const files = collectStagedFiles(repoRoot);
  const nextMessage = buildAutoCommitMessage(files);
  fs.writeFileSync(messageFile, nextMessage, 'utf8');
  return true;
}

function main() {
  const [messageFile, source = '', thirdArg = ''] = process.argv.slice(2);
  const repoRoot = process.cwd();

  try {
    const forceRewrite = thirdArg === '1' || process.env.FORCE_PREPARE_COMMIT_REWRITE === '1';
    const existingMessage = messageFile && fs.existsSync(messageFile)
      ? fs.readFileSync(messageFile, 'utf8')
      : '';

    if (
      forceRewrite
      || shouldAutoGenerate({ source, existingMessage })
    ) {
      applyPrepareCommitMessage({ messageFile, source, repoRoot, forceRewrite });
    }
  } catch (error) {
    console.error('[prepare-commit-msg] failed to build commit template');
    console.error(error instanceof Error ? error.message : error);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = {
  AUTO_GENERATED_MARKER,
  applyPrepareCommitMessage,
  buildAutoCommitMessage,
  classifyPath,
  collectStagedFiles,
  hasUserContent,
  parseNumstat,
  shouldAutoGenerate,
  summarizeFile
};
